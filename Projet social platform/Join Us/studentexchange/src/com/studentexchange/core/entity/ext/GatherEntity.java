package com.studentexchange.core.entity.ext;

public class GatherEntity {
	private String name;
	private Float quota;
	private Float item;
	private long itemid;
	private Float amount;
	private Float sum;
	private Float lf;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Float getQuota() {
		return quota;
	}

	public void setQuota(Float quota) {
		this.quota = quota;
	}

	public Float getItem() {
		return item;
	}

	public void setItem(Float item) {
		this.item = item;
	}

	public long getItemid() {
		return itemid;
	}

	public void setItemid(long itemid) {
		this.itemid = itemid;
	}

	public Float getAmount() {
		return amount;
	}

	public void setAmount(Float amount) {
		this.amount = amount;
	}

	public Float getSum() {
		return sum;
	}

	public void setSum(Float sum) {
		this.sum = sum;
	}

	public Float getLf() {
		return lf;
	}

	public void setLf(Float lf) {
		this.lf = lf;
	}

}
